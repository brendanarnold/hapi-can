const { expect } = require('code')
const { describe, it } = exports.lab = require('lab').script()

describe(`log.js`, async () => {
  describe(`trace`, async () => {
    it(`should log the passed message to STDOUT`)
  })

  describe(`startTime`, async () => {
    it(`should return an object`)
    describe(`given that log() is called on the returned object`, async () => {
      it(`should log the passed message to STDOUT`)
      it(`should log the elapsed time in milliseconds to STDOUT`)
    })

    describe(`given that elapsed() is called on the returned object`, async () => {
      it(`should return the elapsed time in milliseconds`)
    })
  })
})
